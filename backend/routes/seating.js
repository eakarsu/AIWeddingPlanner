const express = require('express');
const pool = require('../db');
const auth = require('../middleware/auth');
const router = express.Router();

router.get('/', auth, async (req, res) => {
  try {
    const result = await pool.query('SELECT * FROM seating WHERE user_id = $1 ORDER BY table_number ASC', [req.user.id]);
    res.json(result.rows);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.get('/:id', auth, async (req, res) => {
  try {
    const result = await pool.query('SELECT * FROM seating WHERE id = $1 AND user_id = $2', [req.params.id, req.user.id]);
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json(result.rows[0]);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.post('/', auth, async (req, res) => {
  try {
    const { table_name, table_number, capacity, guest_names, notes } = req.body;
    const result = await pool.query(
      'INSERT INTO seating (user_id, table_name, table_number, capacity, guest_names, notes) VALUES ($1,$2,$3,$4,$5,$6) RETURNING *',
      [req.user.id, table_name, table_number, capacity, guest_names, notes]
    );
    res.json(result.rows[0]);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.put('/:id', auth, async (req, res) => {
  try {
    const { table_name, table_number, capacity, guest_names, notes } = req.body;
    const result = await pool.query(
      'UPDATE seating SET table_name=$1, table_number=$2, capacity=$3, guest_names=$4, notes=$5 WHERE id=$6 AND user_id=$7 RETURNING *',
      [table_name, table_number, capacity, guest_names, notes, req.params.id, req.user.id]
    );
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json(result.rows[0]);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.delete('/:id', auth, async (req, res) => {
  try {
    const result = await pool.query('DELETE FROM seating WHERE id = $1 AND user_id = $2 RETURNING *', [req.params.id, req.user.id]);
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json({ message: 'Deleted successfully' });
  } catch (err) { res.status(500).json({ error: err.message }); }
});

module.exports = router;
